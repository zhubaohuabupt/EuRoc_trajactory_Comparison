%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%函属功能：旋转矩阵转RPY角%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
function [rr,pp,yy]=getRPY(detaT)
R=detaT(1:3,1:3);

pp=atan2(-R(3,1),sqrt( R(1,1)*R(1,1)+R(2,1)*R(2,1) ) )*180/3.14;

rr=atan2(R(2,1),R(1,1))*180/3.14;

yy=atan2( R(3,2),R(3,3))*180/3.14;


end